//library for 8x8 led matrix to display all letters
unsigned char row[8] = {0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80};
void out_loop(unsigned char col[],int c)
{
	if (c == 0)
	{
		for(int i=0;i<500;i++)
		{
			for(int j=0;j<8;j++)
			{
				ROW_PORT = row[j];
				COL_PORT = col[j];
				_delay_ms(1);
			}
		}
		ROW_PORT = 0b00000000;
		COL_PORT = 0b11111111;
		_delay_ms(200);
	} 
	else if (c == 1)
	{
		for(int k=0;k<8;k++)
		{
			for(int i=0;i<50;i++)
			{
				for(int j=0;j<8;j++)
				{
					PORTA = row[j];
					PORTB = ~((~(col[j]))>>k);
					PORTB|= ~(255>>k);
					_delay_ms(1);
				}
			}
		}
		PORTA = 0b00000000;
		PORTB = 0b11111111;
		_delay_ms(200);
	}
}
void A(int c)
{
	unsigned char clm[8] = {0xbd,0xbd,0xbd,0x81,0xbd,0xbd,0xdb,0xe7};
	out_loop(clm,c);
}
